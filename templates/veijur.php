    <div class="articletexte">
        <h3>Loi REEN</h3>
        <p>A la suite du rapport du Sénat, une loi visant à réduire l'empreinte environnementale du numérique (REEN) est promulguée le 15 novemrbe 2021
            <br>Cette loi établit un plan en plusieurs partie
            <ul>
                <li>Sensibiliser</li>
                <li>Limiter le renouvellement</li>
                <li>Favoriser des usages numériques écologiquement vertueux</li>
                <li>Promouvoir des datacenters et des réseaux moins énergivores</li>
                <li>Promouvoir une stratégie numérique responsable dans les territoires</li>
            </ul>
        </p>
        <a href="https://www.vie-publique.fr/loi/278056-loi-15-novembre2021-reen-reduire-empreinte-environnementale-du-numerique">La loi REEN</a><br>
