        <h3>Introduction</h3>
        <p>La croissance du numérique se traduit par l'utilisation d'une quantité croissante de métaux qui est encore aujourd'hui très peu recyclé. Or l'extraction de ces métaux demande une grande quantité d'eau et d'énergie.
            <br>Par conséquent, il est important de mesurer cet impact afin de réguler à l'avenir le domaine du numérique.
            <br>Cet impact s'appelle l'empreinte environnementale du numérique et est apparu en 2019 lors d'un débat portant sur "Les réseaux du futurs". 
        </p>
    </div>
</div>

<div class="fond article">
    <!-- <img src="./images/accueil.jpg" class="imgarticle"> -->
    <div class="articletexte">
        <h3>Rapport du Sénat</h3>
        <p>Dans un rapport d'information du Sénat paru en 2019, nous informe que le numérique serait à l'origine de 3,7% des émissions totales de gaz à effet de serre (GES) en 2018, dont 44% serait due à la fabrication des terminaux et 56% à leur utilisation.
            <br>La consommation mondiale d'énergie primaire serait également de 4,2%. 
            <br> Si aucune politique publique de sobriété numérique n'est déployée, la part d'emission de GES pourrait grimper à 6,7% en 2040, soit une augmentation de 60%.
        </p>
        <a href="https://datacenter-transition.com/wp-content/uploads/2020/06/20200624_Conf_Dev_Dur_RI_empreinte_environnementale.pdf"> Rapport du sénat </a>
    </div>
</div>

