        <h3>Internet</h3>
        <p>Internet est le plus grand WAN (World Area Network), puisqu'il est à l'échelle mondiale. Néanmoins la plupart des fournisseurs d'accès à Internet (FAI) sont limités dans la diffusion de l'Internet puisque celle-ci se fait majoritairement par des voies terrestres. Ces limitations peuvent être géographiques ou techniques. Par conséquent, certains foyers se retrouvent avec une connexion internet limitée voire inexistante.
            <br>C'est pour cela que certaines entreprises ont pour objectif de rendre Internet accessible depuis n'importe où dans le monde. 
        </p>
