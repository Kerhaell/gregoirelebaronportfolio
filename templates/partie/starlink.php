        <h3>Qu'est ce que Starlink ?</h3>
        <p>L'entreprise Space X ambitionne d'offrir un accès à Internet partout dans le monde, et surtout dans les zones reculées comme les milieux montagnards et désertiques. Pour cela, Space X fonde le projet Starlink. Leur objectif est d'envoyer 12 000 satellites d'ici 2025 en orbite basse. Aujourd'hui, l'entreprise comptabilise déjà, avec ses 3 000 satellites opérationnels, plus de 700 000 clients dans le monde. 
            <br> Chaque client reçoit une antenne qu'il peut installer où il le souhaite. Cette antenne va communiquer avec les satellites de l'entreprise pour permettre une connexion à Internet.
        </p>
