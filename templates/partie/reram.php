        <h3>La mémoire</h3>
        <p>Un des éléments nécessaires à un ordinateur, nous retrouvons la carte-mère, le processeur mais aussi la mémoire.<br>
        Nous retrouvons deux types de mémoires :
    <ul>
        <li>Mémoire volatile (Random Access Memory) : elle est utilisée par le processeur pour stocker temporairement des données nécessaires pour exécuter un programme. Ce type de mémoire perd ses données lorsque le poste est hors tension.</li>
        <li>Mémoire Non-Volatile (Read Only Memory) : elle est utilisée pour stocker les données contenues dans le poste. Ces informations sont écrites physiquement dans des cellules individuelles en language binaire (0 ou 1). Par conséquent, ce stockage de donnéees ne dépend pas du courant.</li>
    </ul>
        Ces deux types de mémoires sont divisés en deux éléments au sein d'un poste. Nous retrouvons les barrettes de RAM pour la mémoire volatile qui se trouve très proche du processeur, puis nous retrouvons une mémoire Flash (Solid State Drives) ou disque dur (Hard Drive Disk) pour la mémoire non volatile. 
    </p>

