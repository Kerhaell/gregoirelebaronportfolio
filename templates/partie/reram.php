        <h3>Une fusion ?</h3>
        <p>Nous assistons à une rapide évolution de la mémoire ces dernières décennies, que ce soit dans la miniaturisation, le stockage, la durée de vie, il est donc légitime de se demander quel est l'avenir de la mémoire. 
            <br> L'objectif est de réunir ces deux mémoires en un seul élément. La Resistive Random-Access Memory (ReRAM) est une des voies explorées pour l'avenir
        <br> En effet la ReRAM offre la possibilité de réunir la RAM et ROM en un seul élément avec des avantages non négligeables. 
    </p>
