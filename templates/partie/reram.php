    <div class="articletexte">
        <h3>Les avantages</h3>
        <p><ul>
            <li> Il est très facile de calculer la résistance (R exprimé en Ohm) d'une résistance avec la loi d'Ohm U = R x I avec U la tension (exprimé en volt) et I la tension (exprimé en ampère) et demande donc moins de ressources en lecture. </li>
            <li> Les données sont sauvegardées en cas de mise hors tension </li>
            <li> Chaque resistance suit un système quaternaire (0,1,2,3) augmentant la capacité de mémoire.</li>
        </p>
