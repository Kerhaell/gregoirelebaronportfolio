    <div class="articletexte">
        <h3>Feedly</h3>
        <p>Feedly est un agrégateur de flux RSS (Really Simple Syndication), c'est à dire qu'il reçoit automatiquement des contenues provenant d'autres sites web dès lors qu'ils sont mis à jour. 
            <br> Feedly est à la fois une application Web et mobile et permet à l'utilisateur de trier les flux en fonction de mots clés. 
            <br> Une version payante est disponible afin de passer à un niveau de trie supérieur.
        </p>
        <a href="https://feedly.com/">Le lien </a>
    </div>
</div>


<div class="fond article">
    <img src="./images/google_alerts.jpg" class="imgarticle">
    <div class="articletexte">
        <h3>Google Alerts</h3>
        <p>Google Alerts fonctionne différemment par rapport à Feedly. En effet, Google Alerts propose de créer des alertes basées sur des mots-clés et par la suite nous envoyer un mail quotidien répertoriant les pages contenant ces mots qui sont publiées sur la Toile.
            <br>Plusieurs options existent, comme par exemple la fréquence des mails, la qualité des résultats...
            <br>Néanmoins, il n'est pas possible de remonter dans le temps.
        </p>
        <a href="https://www.google.fr/alerts">Le lien </a>
    </div>
</div>